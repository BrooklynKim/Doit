package chapter1;

public class Variable02 {

	public static void main(String[] args) {
		
		int level = 10; // level 변수 선언과 동시에 값을 대입(초기화)
		System.out.println(level);
		
		int year;
		year = 2018;
		System.out.println(year);
		
		int month = 05;
		System.out.println(month);

	}

}
